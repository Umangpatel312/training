package com.bean;

public class TwoDigit {
	private int digit1;
	private int digit2;
	public TwoDigit(){
		
	}
	public TwoDigit(int digit1, int digit2) {
		super();
		this.digit1 = digit1;
		this.digit2 = digit2;
	}
	public int getDigit1() {
		return digit1;
	}
	public void setDigit1(int digit1) {
		this.digit1 = digit1;
	}
	public int getDigit2() {
		return digit2;
	}
	public void setDigit2(int digit2) {
		this.digit2 = digit2;
	}
	
}
